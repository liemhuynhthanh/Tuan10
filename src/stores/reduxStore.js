import { createStore } from "redux";

// Reducer thuáº§n
const initialState = { count: 0 };

function counterReducer(state = initialState, action) {
  switch (action.type) {
    case "INCREMENT":
      return { count: state.count + 1 };
    case "DECREMENT":
      return { count: state.count - 1 };
    default:
      return state;
  }
}

const reduxStore = createStore(counterReducer);

export default reduxStore;
